x = 2
y = 5

# Suma
suma = x + y
print("Suma:", suma)
# Resta
resta = x - y
print("Resta:", resta)
# Multiplicación
multiplicacion = x * y
print("Multiplicación:", multiplicacion)
# División
division = x / y
print("División:", division)
# División entera
division_entera = x // y
print("División entera:", division_entera)
# Módulo
modulo = x % y
print("Módulo:", modulo)
# Potenciación
potenciacion = x ** y
print("Potenciación:", potenciacion)

